class poit2D {
    float x;
    float y;
    public poit2D(){
        this.x = 0.0f;
        this.y = 0.0f;
    }
    public poit2D(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
    public void setXY(float x, float y){
        this.x= x;
        this.y = y;
    }



}
